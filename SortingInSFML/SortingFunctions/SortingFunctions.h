/// <summary>
/// Sort an array of values in a Bubble Sort way.
/// In steps implies that after each value that is
/// sorted, the function will stop its execution.
/// </summary>
/// <param name="values">An array of values.</param>
/// <param name="size">The size of the array.</param>
void bubbleSortInSteps(int values[], int size);

/// <summary>
/// Sort an array of values in a Insertion Sort way.
/// In steps implies that after each value that is
/// sorted, the function will stop its execution.
/// </summary>
/// <param name="values">An array of values.</param>
/// <param name="size">The size of the array.</param>
void insertionSortInSteps(int values[], int size);

/// <summary>
/// Sort an array of values in a Selection Sort way.
/// In steps implies that after each value that is
/// sorted, the function will stop its execution.
/// </summary>
/// <param name="values">An array of values.</param>
/// <param name="size">The size of the array.</param>
void selectionSortInSteps(int values[], int size);

/// <summary>
/// Sort an array of values in a Shell Sort way.
/// In steps implies that after each value that is
/// sorted, the function will stop its execution.
/// </summary>
/// <param name="values">An array of values.</param>
/// <param name="size">The size of the array.</param>
void shellSortInSteps(int values[], int size);